  end

  def updateCollaborators(project,collabslist)
    project.collaborators = []
    collabslist.each do |collab|
      collaborator = Collaborator.find_by_id(collab)
      if collaborator
        collaborator.projects << project if not collaborator.projects.include?(project)
      end
    end
    project.save
  end

  # POST /projects
  # POST /projects.json
  def create
    collabParam = params[:project][:collaborators]
    @project = Project.new(params[:project])

    respond_to do |format|
      if @project.save
        format.html { redirect_to @project, notice: 'Project was successfully created.' }
        format.json { render json: @project, status: :created, location: @project }
      else
        format.html { render action: "new" }
        format.json { render json: @project.errors, status: :unprocessable_entity }
      end
    end
  end

